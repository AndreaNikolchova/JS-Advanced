function solve(x,y,operator){
    if(operator ==="+")
    {
        console.log(x+y);
    }
    else if(operator ==="-")
    {
        console.log(x-y);
    }
    else if(operator ==="*")
    {
        console.log(x*y);
    }
    else if(operator ==="/")
    {
        console.log(x/y);
    }
    else if(operator ==="%")
    {
        console.log(x%y);
    } else if(operator ==="**")
    {
        console.log(x**y);
    }
}